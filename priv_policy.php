<h2>Your Rights Regarding Your Personal Information:</h2>

<h3>Information Access</h3>

<p>You may request confirmation of whether or not The  School is processing personal information about you. If so, you have the right to see or obtain a copy of that information and to learn: for what purpose(s) we are processing that information; the categories of information; the identity of those who receive the information; if we receive personal information about you from other sources, any available information about those sources; and how long we keep the information or, if the period is not determined, the factors that we use to determine that period.</p>

<h3>Correction and Completion</h3>

<p>You have the right to request correction of personal information that you believe is inaccurate. Where you believe the information is incomplete, you have the right to complete the information, including by supplementing the information with a statement.</p>

<h3>Right of Erasure or &ldquo;To be Forgotten&rdquo;</h3>

<p>You have the right to request that The  School permanently delete your personal information when:</p>

<ul>
	<li>we no longer need your information for the purposes for which it was collected;</li>
	<li>we have been processing the particular information based only on your consent, you withdraw your consent, and there is no other legal ground for us to continue processing;</li>
	<li>you successfully object to the processing of certain information (see &ldquo;Objection&rdquo; below);</li>
	<li>the information has been unlawfully processed; or</li>
	<li>erasure is required by law.</li>
</ul>

<p>This right will not apply where continued processing of your information is necessary for:</p>

<ul>
	<li>the exercise of the right of freedom of expression and information;</li>
	<li>compliance with applicable law;</li>
	<li>performance of a task carried out in the public interest or the exercise of official authority vested in The  School;</li>
	<li>ensuring the functionality and stability of said system;</li>
	<li>for reasons of public health;</li>
	<li>certain types of archiving that is in the public interest, scientific or historical research or statistical purposes; or</li>
	<li>the establishment, exercise or defense of legal claims.</li>
</ul>

<p>Although The  School may remove most personal information such as contact information (address, phone number, etc.), there may be information we cannot remove, including, but not limited to, certain records such as the user&rsquo;s attendance at The School and their grades or information about certain donations made to the school.</p>

<h3>Restriction on Processing</h3>

<p>You have the right to request that The  School stop or restrict the processing of your personal information where:</p>

<ul>
	<li>you are contesting the accuracy of your personal information, in which case processing could be restricted for the period of time necessary for us to determine the accuracy of the information;</li>
	<li>the processing is unlawful and you oppose the erasure of the personal information and request instead that processing be restricted;</li>
	<li>we no longer need the personal information for the purposes of the processing in question, but you need the information for the establishment, exercise or defense of legal claims; or</li>
	<li>you object to the processing of certain information (see &ldquo;Objection&rdquo; below), in which case processing could be restricted for the period of time necessary for us to determine our response to the objection.</li>
