<p>You also may send us personally identifying information, for example, in an email message containing a question or comment, or by completing a web form that provides us with this information. We use personally identifying information from these communications primarily to respond to you. We may forward your correspondence to school representatives who are better able to answer your questions. We may also use your email to contact you in the future about our programs that we deem may be of interest to you. We do not share your name or email address with any third parties.</p>

