<p>With respect to personal information that we have received directly from you (as opposed to information that we may receive about you from sources other than you), you have a right to request and receive such information in a structured, commonly used and machine-readable format. You also have the right to transmit such information to another entity without interference from us.</p>

<p>Please note that this right of portability applies only in limited circumstances, such as: where we are processing personal information on the basis of your consent or pursuant to our contractual agreements with you, and the processing is carried out by automated means. If you request that we transmit your information directly to another individual or entity, we will comply where it is feasible to do so.</p>

<h3>Objection</h3>

<p>You may object to processing of your personal information that is based on The  School&rsquo;s determination that such processing is necessary for the performance of a task carried out in the public interest or in the exercise of official authority, or that such processing is necessary for the purposes of the legitimate interests pursued by The School.</p>

<p>If we process your personal information for purposes of direct marketing, you may object at any time. Direct communications from The  School that are for marketing, promotional, or solicitation purposes may contain an option for unsubscribing and if so, will provide instructions for how to do so.</p>

