<p>If you successfully exercise this right of restriction, we will process the information in question only: (i) where we have your consent to do so; (ii) for the establishment, exercise or defense of legal claims; (iii) for the protection of the rights of another person; or (iv) for reasons of important public interest. In the event that any such restriction is lifted and processing resumes, we will inform you beforehand.</p>

