<h2>PRIVACY NOTICE</h2>
<p>Last updated <span style="color: #8351f9;">[5-12-2023]</span></p>
<p>This privacy notice for <span style="color: #8351f9;">[Synergy School System]</span>

<p><strong>Questions or concerns?</strong> Reading this privacy notice will help you understand your privacy rights and choices. If you do not agree with our policies and practices, please do not use our Services. If you still have any questions or concerns, please contact us at <span style="color: #8351f9;">[0995619722]
<h3>SUMMARY OF KEY POINTS</h3>
<p>The School is committed to protecting the privacy and information it collects from its community. This policy explains how The  School collects and handles information it receives from applicants and their families, students and their families, alumni, employees (including applicants for employment) and others who interact with The  School or its website. Other than IP addresses and traffic information collected described below, we do not collect personally identifying information on our website unless you choose to provide such information to us.</p>

<p>This policy sets forth the type of information we collect, how it is stored and used, and the rights and options you may have with respect to your personal information.</p>

<h2>What Personal information We Collect; How and When We Collect It</h2>

<p>Our goal is to collect the minimum amount of information necessary - whether from applicants, enrolled students, alumni, employees, or contractors.</p>

<p>We collect information you provide us. A person&rsquo;s interaction with The  School occurs during the admissions process, during matriculation (and throughout a student&rsquo;s enrollment at The  School), and as alumni. We also collect information from applicants for employment as well as employees and contractors of the school.</p>

<p>It is important to the School to protect the privacy of young children who may use our website. The School does not knowingly solicit or collect the personal information of children under the age of 13 on this website. Children under the age of 13 are not permitted to submit an inquiry form or application to the School. Parents or legal guardians of a child under the age of 13 must complete the inquiry form and consent to the submission of an application to the School.</p>

<h2>Usage Information, Site Activity and Location Information</h2>

<p>We automatically collect information regarding the actions you take on the website (&quot;Usage Information&quot;). Usage Information helps us understand trends in our users&rsquo; needs and improve our website and its functionality. We also may use Usage Information for general marketing purposes. This Privacy Policy in no way restricts or limits our collection and use of Usage Information, and we may share Usage Information about our users with third parties for various purposes, including to help us better understand your needs and improve our website. When you use our website and/or provide us with information, some examples of the Usage Information we may collect include: the type of Web browser you use, your operating system, your web requests, your Internet Service Provider, your IP address, referring/exit pages and URLs, the pages you view and how you interact with links on our website, comments and other content you post, the time and duration of your visits to the website and other such information. We may store such Usage Information itself and/or such information may be included in databases owned and maintained by agents or service providers. We may also collect information that identifies the device you use to access the website, your device&rsquo;s unique device ID, the IP address of your device, your operating system, connection type (e.g. WiFi or cellular) and GPS technology (or other similar technology) to determine your current location when you use a mobile device to access the website. If you do not want us to have your location information, you should turn off the location services before accessing the website from a mobile device. We may use such information and pool it with other information to track, for example, the total number of visitors to our website, the number of visitors to each page of our website, etc. We use this information to help us understand how people use the website and to enhance the website.</p>

<h2>Cookies and Web Beacons</h2>

<p>We use cookies (a small text file placed on your computer to identify your computer and browser). We may also use Web beacons (a file placed on a website that monitors usage). We use cookies and Web beacons to improve the experience of the site and services we provide, such as pre-populating your username for easier login, to allow user-specific messaging or to permit user-specific advertising. We do not use cookies or Web beacons to collect Personal Information. Most Web browsers are initially set up to accept cookies. At this time, the school does not recognize automated browser signals regarding tracking, including &quot;do-not-track&quot; signals. You can remove persistent cookies and change your privacy preferences by following directions provided in your Internet browser&rsquo;s &quot;help&quot; directory. However, certain features of the site or services may not work if you delete or disable cookies. Some of our third party providers may use their own cookies and Web beacons in connection with the services they perform on our behalf.</p>

<h2>Admissions:</h2>

<p>It is expected that information shared with The School during the admission process is given with the consent of a parent or guardian. The school does not knowingly collect information from anyone under the age of 18 without the consent of a parent or guardian.</p>

<p>During the admissions process, personal information will be collected and stored about the applicant and family for the purposes of evaluating the applicant. The  School also will use the information for communicating with the family during the admissions process.</p>

<p>Personal information about applicants, their parents/guardians and siblings will be collected via our website (the Inquiry Form) as well as by The Standard Application Online administered by the Enrollment Management Association or other similar common applications administered by a third party. Additional information may be collected by the admissions office via various interviews, phone calls, references, and emails.</p>

<p>Types of personal information collected include names, addresses, phone numbers, emails, gender, dates of birth, race, ethnicity, nationality, citizenship, educational history, and legacy status. Further, we collect information about the student&rsquo;s academic and disciplinary history, athletic, and extra-curricular records and interests, and possibly financial information (to process financial aid requests). Additional information regarding the applicant&rsquo;s parents or guardians such as employment and marital status also will be collected.</p>

<p>As part of the admissions process, the school also may gather or receive information from other sources regarding medical/educational records, standardized tests, and other evaluative material. The types of information we collect in these cases may be specific to the individual.</p>

<p>We receive and process such personal information pursuant to your agreements with such third-party service providers, and in furtherance of your agreement with us concerning your application and matriculation and pursuant to our legitimate interests in assessing your application. For information on the privacy practices of such third parties, please consult your agreements with them and their own privacy policies.</p>

<p>You are required to provide this information if you would like to apply to or be enrolled at The  School. Without this information, The  School cannot process a student&rsquo;s application or enroll them. To the extent that any requested personal information is not necessary for the performance of The  School&rsquo;s agreements and policies, such requests will be separately identified and the appropriate consent obtained.</p>

<p>Applicant information is kept permanently.</p>

<h2>Matriculation:</h2>

<p>It is expected that information shared with The  School during a student&rsquo;s enrollment is given with the consent of a parent or guardian. The school does not knowingly collect information from anyone under the age of 18 without the consent of a parent or guardian.</p>

<p>Once enrolled, The  School continues to collect and process personal information for the purposes of caring for and educating the student. The personal information is used to keep you informed about the school and to offer you access to school services. Personal information also will be collected, created and stored (about the student and family) for the purposes of tracking student progress at The  School.</p>

<p>The information collected for enrolled students and their families includes names, addresses, phone numbers, emails, gender and dates of birth. Passport information or other identification number issued by the government to verify identity also may be collected. Health and medical information, including health insurance policy and subscriber identification numbers, regarding the student is collected to ensure their well-being while on campus and for medical treatment purposes. Further, we collect information about the enrolled student&rsquo;s academic, athletic, and extra-curricular records and interests, and possibly financial information. Information about performance (classes, co-curricular activities, etc.) and images of the student engaged in academic, athletic, and extra-curricular activities also may be collected and maintained. We may post information about performance and images of the student on the internet and in publications.</p>

<p>The  School may gather or receive information from other sources such as testing agencies (including, medical/educational, standardized tests, etc.). The types of information we collect in these cases may be specific to the individual. We receive and process such information pursuant to your agreements with such third-party service providers, and in furtherance of your agreements with us concerning your matriculation. For information on the privacy practices of such third parties, please consult your agreements with them and their individual policies.</p>

<p>This information collection is required if one wants to be enrolled at The  School. To the extent that any requested personal information is not necessary for the performance of The  School&rsquo;s agreements and policies, such requests will be separately identified and the appropriate consent obtained.</p>

<p>Student records are kept permanently.</p>

<h2>Alumni and Friends:</h2>

<p>Alumni, friends and parents are lifelong members of The  School community. The Development Office of the  School is dedicated to keeping our alumni, friends and parents (past and present) connected to the school.</p>

<p>Types of information collected include name, address, phone number, email, gender, date of birth, employment, gifts received, and events attended. Credit card numbers are collected and used only for donation or payment processing through an encrypted, secure, third party processing system, and are not retained for other purposes.</p>

<p>The information we collect is used for a variety of reasons, including:</p>

<ul>
	<li>Directory information, including the online alumni community.</li>
	<li>Communicating with you to let you know about receptions, reunions, and other events with alumni or  faculty/administrators in the area.</li>
	<li>Making you aware of projects and opportunities to support the school (via volunteering, gifts, etc.)</li>
	<li>General Fundraising</li>
	<li>Surveys</li>
	<li>Promoting services we may offer to alumni.</li>
</ul>

<p>Since The  School likes to keep in contact with you for life, we retain these records permanently.</p>

<p>For any questions regarding the collection or use of alumni information, please contact privacy@school.org</p>

<h2>Applicants for Employment, Employees and Contractors:</h2>

<p> School collects information from applicants for employment, employees and contractors of the school. The school uses an employment application to collect personal information such as biographical information, gender, date of birth, employment history, and employment/personal references on all applicants for employment. This information becomes part of the employee&rsquo;s file once hired.  School also conducts extensive criminal background checks through fingerprinting and if applicable, a credit report, on its employees. Upon hire and throughout employment at the school, the employee may provide additional information to the school such as bank information, social security numbers or other governmental identification number commonly used to verify identity, passport information, biometric information for time-keeping purposes, health and medical information, including health insurance policy and subscriber identification numbers, beneficiary and dependent information, and other information related to such employment. The school collects EIN or social security numbers from contractors and may collect bank information for payment purposes. Certain contractors with direct, unsupervised access to students will need to provide personal information such as biographical information, date of birth, employment history, and employment/personal references. The school also will conduct criminal background checks through fingerprinting of such contractors with direct, unsupervised access to students.</p>

